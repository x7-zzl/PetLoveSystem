package model;


public class Master {
    private int masterId;
    private String masterName;
    private int masterAge;
    private String masterSex;

    public int getMasterId() {
        return masterId;
    }

    public void setMasterId(int masterId) {
        this.masterId = masterId;
    }

    public String getMasterName() {
        return masterName;
    }

    public void setMasterName(String masterName) {
        this.masterName = masterName;
    }

    public int getMasterAge() {
        return masterAge;
    }

    public void setMasterAge(int masterAge) {
        this.masterAge = masterAge;
    }

    public String getMasterSex() {
        return masterSex;
    }

    public void setMasterSex(String masterSex) {
        this.masterSex = masterSex;
    }
}
